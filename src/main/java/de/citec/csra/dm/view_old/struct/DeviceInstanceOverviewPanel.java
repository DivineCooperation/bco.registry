	@Override
	protected List<DeviceInstance> getContextList() {
		return deviceManager.getDeviceInstanceList();
	}

	@Override
	protected String[] getContextLables() {
		String[] contextLables = {"ID", "Name", "Class", "SerialNumber", "Location", "Status", "Description"};
		return contextLables;
	}

	@Override
	protected void updateContextData(final DeviceInstance context, final Object[] contextData) {
		contextData[0] = context.getId();
		contextData[1] = context.getName();
		contextData[2] = context.getDeviceClass();
		contextData[3] = context.getSerialNumber();
		contextData[4] = context.getLocation();
		contextData[5] = context.getStatus();
		contextData[6] = context.getDescription();
	}

	@Override
	protected void remove(final DeviceInstance deviceInstance) {
		deviceManager.removeDeviceInstance(deviceInstance.getId());
	}

	@Override
	protected void edit(final DeviceInstance deviceInstance) {
		DeviceInstanceEditorFrame.edit(deviceInstance);
	}

	@Override
	protected void add() {
		DeviceInstance deviceInstance = new DeviceInstance();
		edit(deviceInstance);
	}

	@Override
	protected DeviceInstance getSelection(final JTable contextTable) throws NotAvailableException {
                //selectedColumn = contextTable.getSelectedColumn();
		return deviceManager.getDeviceInstance((String) contextTable.getModel().getValueAt(contextTable.convertRowIndexToModel(contextTable.getSelectedRow()), 0));
	}
